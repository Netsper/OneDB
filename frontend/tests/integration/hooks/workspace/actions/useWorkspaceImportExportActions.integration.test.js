import test from 'node:test';
import assert from 'node:assert/strict';
import createWorkspaceImportExportActions from '../../../../../src/hooks/workspace/actions/useWorkspaceImportExportActions.js';

function setupDomDownloadMocks() {
  const originalDocument = globalThis.document;
  const originalCreateObjectURL = globalThis.URL?.createObjectURL;

  let capturedBlob = null;
  const capturedAnchors = [];

  globalThis.document = {
    body: {
      appendChild: () => {},
      removeChild: () => {},
    },
    createElement: (tag) => {
      assert.equal(tag, 'a');
      const attrs = {};
      const anchor = {
        attrs,
        setAttribute: (key, value) => {
          attrs[key] = value;
        },
        click: () => {
          attrs.__clicked = true;
        },
      };
      capturedAnchors.push(anchor);
      return anchor;
    },
  };

  if (!globalThis.URL) {
    globalThis.URL = class URL {};
  }

  globalThis.URL.createObjectURL = (blob) => {
    capturedBlob = blob;
    return 'blob:onedb-test';
  };

  const restore = () => {
    globalThis.document = originalDocument;

    if (originalCreateObjectURL) {
      globalThis.URL.createObjectURL = originalCreateObjectURL;
    } else if (globalThis.URL?.createObjectURL) {
      delete globalThis.URL.createObjectURL;
    }
  };

  return {
    getCapturedBlob: () => capturedBlob,
    getCapturedAnchors: () => capturedAnchors,
    restore,
  };
}

